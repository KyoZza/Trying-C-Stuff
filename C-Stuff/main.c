char getMaxCharVal(const char* str, const unsigned short len) {
	char max = 0;

	for (unsigned short i = 0; i < len; i++)
		max = max < str[i] ? str[i] : max;

	return max;
}

void getMinMaxCharVal(const char* str, const unsigned short len, char* min, char* max) {
	for (unsigned short i = 0; i < len; i++) {
		*max = *max < str[i] ? str[i] : *max;
		*min = *min > str[i] ? str[i] : *min;
	}
}

char firstRecurringChar(const char* str, const unsigned short len) {
	char min = INT8_MAX;
	char max = 0;

	//char arrLength = getMaxCharVal(str, len);
	//char* arr = (char*)malloc(sizeof(char) * arrLength);
	getMinMaxCharVal(str, len, &min, &max);
	
	char deltaMax = max - min;
	char* arr = (char*) malloc(sizeof(char) * deltaMax);

	// Set values to 0
	for (unsigned short i = 0; i <= deltaMax; i++)
		arr[i] = 0;

	// Search for first recurring char
	for (unsigned short i = 0; i < len; i++) {
		if (++arr[str[i]-min] == 2) return str[i];
	}

	return NULL;
}

char firstNonRecurringChar(const char* str, const unsigned short len) {
	char min = INT8_MAX;
	char max = 0;

	//char arrLength = getMaxCharVal(str, len);
	//char* arr = (char*)malloc(sizeof(char) * arrLength);
	getMinMaxCharVal(str, len, &min, &max);

	char deltaMax = max - min;
	char* arr = (char*)malloc(sizeof(char) * deltaMax);

	// Set values to 0
	for (unsigned short i = 0; i <= deltaMax; i++)
		arr[i] = 0;

	// Count occurances 
	for (unsigned short i = 0; i < len; i++)
		++arr[str[i]-min];

	// Search for first non recurring char
	for (unsigned short i = 0; i < len; i++) {
		if (arr[str[i]-min] == 1) return str[i];
	}

	return NULL;
}

void cyclicRotation1(int* A, int N, int K) {
	if (N == 0 || K == N) return;

	for (int i = 0; i < K; i++) {
		int last = A[N - 1];
		int* p = A;
		int move = *p;

		while (p != (A + N - 1)) {
			int tmp = *(p + 1);
			*(p + 1) = move;
			move = tmp;

			p++;
		}

		A[0] = last;
	}
}


